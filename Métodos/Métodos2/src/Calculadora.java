public class App {
    public static void soma (double num1, double num2) {
        double resultado = num1 + num2;
        System.out.println("A soma dos valores é igual a " + resultado);
    }

    public static void subtracao (double num1, double num2){
        double resultado = num1 - num2;
        System.out.println("A subtração dos valores é igual a " + resultado);
    }

    public static void multiplicacao (double num1, double num2) {
        double resultado = num1 * num2;
        System.out.println("A multiplicação dos valores é igual a " + resultado);
    }

    public static void divisao (double num1, double num2) {
        double resultado = num1 / num2;
        System.out.println("A divisão dos valores é igual a " + resultado);
    }
}
